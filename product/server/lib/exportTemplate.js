/* private */

/**
 * Returns the effective value for an item following the agreed rule:
 *   userOverrideValue ?? estimatedValue.estimate ?? null
 */
function _effectiveValue(item) {
  if (item.userOverrideValue != null && item.userOverrideValue !== '') {
    const v = parseFloat(item.userOverrideValue)
    return isNaN(v) ? null : v
  }
  if (item.estimatedValue && item.estimatedValue.estimate != null) {
    const v = parseFloat(item.estimatedValue.estimate)
    return isNaN(v) ? null : v
  }
  // Legacy fallback: support items that still carry currentValue (migration period)
  if (item.currentValue != null && item.currentValue !== '') {
    const v = parseFloat(item.currentValue)
    return isNaN(v) ? null : v
  }
  return null
}

function _buildItemRow(item) {
  const photoCell = item.photoPath
    ? `<img src="${_escapeAttr(item.photoPath)}" alt="Item photo" />`
    : `<div class="no-photo">No<br>Photo</div>`

  const purchasePrice =
    item.purchasePrice != null && item.purchasePrice !== ''
      ? parseFloat(item.purchasePrice).toLocaleString('en-US', {
          style: 'currency',
          currency: 'USD',
        })
      : '&mdash;'

  const effectiveVal = _effectiveValue(item)

  let marketValueCell
  if (effectiveVal !== null) {
    const formatted = effectiveVal.toLocaleString('en-US', {
      style: 'currency',
      currency: 'USD',
    })
    const evidenceLine = _buildEvidenceLine(item)
    marketValueCell = `${formatted}<div class="evidence">${evidenceLine}</div>`
  } else {
    marketValueCell =
      '&mdash;<div class="evidence">Not yet valued (add comp data for this model to generate an estimate)</div>'
  }

  return `      <tr>
        <td class="photo-cell">${photoCell}</td>
        <td>${_escapeHtml(item.make || '—')}</td>
        <td>${_escapeHtml(item.model || '—')}</td>
        <td>${_escapeHtml(item.serial || '—')}</td>
        <td>${_escapeHtml(item.condition || '—')}</td>
        <td>${_escapeHtml(item.purchaseDate || '—')}</td>
        <td class="value">${purchasePrice}</td>
        <td class="value">${marketValueCell}</td>
      </tr>`
}

/**
 * Builds the evidence line shown beneath the market value in each row.
 * Shows comp provenance or override notice.
 */
function _buildEvidenceLine(item) {
  const hasOverride =
    item.userOverrideValue != null && item.userOverrideValue !== ''
  const ev = item.estimatedValue

  if (hasOverride) {
    if (ev && ev.sampleSize > 0 && ev.asOf) {
      return _escapeHtml(
        `User override applied · comp estimate based on ${
          ev.sampleSize
        } recent sale${ev.sampleSize === 1 ? '' : 's'} as of ${
          ev.asOf
        }; range $${_fmt(ev.low)}–$${_fmt(ev.high)}`
      )
    }
    return _escapeHtml('User override applied')
  }

  if (ev && ev.sampleSize > 0 && ev.asOf) {
    return _escapeHtml(
      `comp-backed; based on ${ev.sampleSize} recent sale${
        ev.sampleSize === 1 ? '' : 's'
      } as of ${ev.asOf}; range $${_fmt(ev.low)}–$${_fmt(ev.high)}`
    )
  }

  return _escapeHtml('comp-backed estimate')
}

/** Format a number as a plain dollar amount (no symbol, 2 decimal places). */
function _fmt(n) {
  if (n == null) return '—'
  return parseFloat(n).toLocaleString('en-US', {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  })
}

/**
 * Escapes a string for safe insertion as HTML text content.
 * Handles &, <, >, ", and ' characters.
 */
function _escapeHtml(str) {
  if (str == null) return ''
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}

/**
 * Escapes a string for safe use inside a double-quoted HTML attribute value.
 * Used specifically for src="..." to prevent attribute injection.
 */
function _escapeAttr(str) {
  if (str == null) return ''
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
}

module.exports = { buildInsuranceHtml }
